// Arithmetic operators
#include <iostream>

using namespace std;

int main() {

    int a, b;
    cin >> a >> b;

    cout << "both int:\n";
    cout << "a + b = " << a + b << endl;
    cout << "a - b = " << a - b << endl;
    cout << "a * b = " << a * b << endl;
    cout << "a / b = " << a / b << endl;
    cout << "a % b = " << a % b << endl;

    return 0;
}